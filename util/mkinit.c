int 
main(int argc, char **argv)
{
	progname = argv[0];

	parse_options(argc, argv);

	output_headers();
	output_sub_init_functions();
	output_main_init_function();
	output_main();

	if (num_errors > 0) {
		fputs("/* Force syntax error, since there were */\n", stdout);
		fputs("/* errors in the generation of this file */\n", stdout);
		fputs("#error \"You need to remake this file\"\n", stdout);
		return EXIT_FAILURE;
	}

	return EXIT_SUCCESS;
}

/*---------------------------------------------------------------------------*/

static void 
parse_options(int argc, char *argv[])
{
	int	c;
	while ((c = getopt(argc, argv, "c:iltw:x")) != EOF) {
		switch (c) {
		case 'c':
			if (sscanf(optarg, "%d", &maxcalls) != 1)
				usage();
			break;

		case 'i':
			need_initialization_code = TRUE;
			break;

		case 'l':
			output_main_func = FALSE;
			break;

		case 't':
			need_tracing = TRUE;
			need_initialization_code = TRUE;
			break;

		case 'w':
			entry_point = optarg;
			break;

		case 'x':
			c_files_contain_extra_inits = TRUE;
			break;

		default:
			usage();
		}
	}
	num_files = argc - optind;
	if (num_files <= 0)
		usage();
	files = argv + optind;
}

static void 
usage(void)
{
	fprintf(stderr,
		"Usage: mkinit [-c maxcalls] [-w entry] [-i] [-l] [-t] [-x] files...\n");
	exit(1);
}

/*---------------------------------------------------------------------------*/

static void 
output_headers(void)
{
	int filenum;

	fputs(header1, stdout);

	for (filenum = 0; filenum < num_files; filenum++) {
		fputs("** ", stdout);
		fputs(files[filenum], stdout);
		putc('\n', stdout);
	}

	fputs(header2, stdout);
}

static void 
output_sub_init_functions(void)
{
	int filenum;

	if (! need_initialization_code) {
		fputs(if_need_to_init, stdout);
	}

	fputs("static void init_modules_0(void)\n", stdout);
	fputs("{\n", stdout);

	for (filenum = 0; filenum < num_files; filenum++) {
		process_file(files[filenum]);
	}

	fputs("}\n", stdout);
	if (! need_initialization_code) {
		fputs("\n#endif\n", stdout);
	}
}

static void 
output_main_init_function(void)
{
	int i;

	fputs("\nstatic void init_modules(void)\n", stdout);
	fputs("{\n", stdout);

	if (! need_initialization_code) {
		fputs(if_need_to_init, stdout);
	}

	for (i = 0; i <= num_modules; i++) {
		printf("\tinit_modules_%d();\n", i);
	}

	if (! need_initialization_code) {
		fputs("\n#endif\n", stdout);
	}

	fputs("}\n", stdout);
}

static void 
output_main(void)
{
	const char *trace_func;

	trace_func = (need_tracing ? "MR_trace_real" : "MR_trace_fake");
	printf(mercury_funcs, entry_point, trace_func, entry_point);
	if (output_main_func) {
		fputs(main_func, stdout);
	}
}

